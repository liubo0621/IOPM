package cn.com.pattek.KeyWords.entity;

import java.util.List;

public class IopmClassify {
	private Long first_classify_id;  //类别
	private String first_classify;   //类别名称
	private List<IopmKeyInfoEntity> list;   //类别包含的关键字实体
	private Long zero_id;     //一级类别ID
	private Long count;	 //关键字数量
	private String image_url; // 图片地址
	private Float weight;
	public Float getWeight() {
		return weight;
	}
	public void setWeight(Float weight) {
		this.weight = weight;
	}
	public Long getFirst_classify_id() {
		return first_classify_id;
	}
	public void setFirst_classify_id(Long first_classify_id) {
		this.first_classify_id = first_classify_id;
	}
	public String getFirst_classify() {
		return first_classify;
	}
	public void setFirst_classify(String first_classify) {
		this.first_classify = first_classify;
	}
	public List<IopmKeyInfoEntity> getList() {
		return list;
	}
	public void setList(List<IopmKeyInfoEntity> list) {
		this.list = list;
	}
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}

	public Long getZero_id() {
		return zero_id;
	}
	public void setZero_id(Long zeroId) {
		zero_id = zeroId;
	}
	public String getImage_url() {
		return image_url;
	}
	public void setImage_url(String imageUrl) {
		image_url = imageUrl;
	}

}
